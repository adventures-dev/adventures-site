 <script type="text/javascript">


                    

//form validation rules
$("#contact-form").validate({
    rules: {
        name:  "required",
        phone: {
            required: true,
        },
        email: {
            required: true,
            email: true
        },
        message:  "required"


    },
    messages: {
       /* username: "<font style='color:red;'>Please enter a username</font>",*/
       name: "<font>Please enter your name</font>",

        phone: "<font>Please enter your phone number</font>",

        email: "<font>Please enter a valid email address</font>",
        message: "<font>Please enter a message</font>",

    },
    submitHandler: function (form) {

        ///ajax here::::

        var email = document.getElementById('email').value;
        var name = document.getElementById('name').value;
        var phone = document.getElementById('phone').value;
         var message = document.getElementById('message').value;

        var data = {
            email: email,
            name: name,
            phone: phone,
            message: message

        };
        
        $.ajax({
            type: "POST",
            url: "contact.php",
            data: data,
            success: function (res) {

                if (res == true) {
                
                	$("#form-content").html("<p>Thanks!  We will get back to you shortly!</p>");

                } else {
                    console.log(res);
                    $("#error").append('<font style="color:red;">' + res + '</font>');
                }

            }
        });
       
    }
});
                        
                    

                
